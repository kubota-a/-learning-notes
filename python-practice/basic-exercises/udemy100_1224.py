# 問題8
# 数字の1から30のうち、3の倍数だけを格納した「リスト」を作成して、表示しましょう。
# 作成したリスト : [3, 6, 9, 12, 15, 18, 21, 24, 27, 30]

# 設計メモ
# 空のリストmultiple_of_3を作成
# for文、range（）で1から30までの数を1つずつ取り出して、変数iに格納
# もしiを3で割り算したときの余りが0なら、リストmultiple_of_3にiを要素として追加
# ループを抜けてから、print（）で”作成したリスト：”とリストmultiple_of_3を出力

multiple_of_3 = []
for i in range(1, 31):
    if i % 3 == 0:
        multiple_of_3.append(i)
print(f'作成したリスト：{multiple_of_3}')

