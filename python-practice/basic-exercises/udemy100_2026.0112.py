# 問題62
# 以下2つのリストで、どちらか一方に格納されている要素を出力するプログラムを作成してください。
# ※使用するリスト :
# ```
# l1 = [1, 2, 3, 4, 5]
# l2 = [7, 6, 5, 4]
# ```
# ▼期待する出力
# ```
# どちらか一方に格納されている要素 : {1, 2, 3, 6, 7}
