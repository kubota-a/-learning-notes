# 問題63
# 以下2つのリストで、片方のリストを含んでいるか判定するプログラムを作成してください。
# ※使用するリスト :
# ```
# l1 = [1, 2, 3, 4, 5]
# l2 = [3, 4, 5]
# ```
# ▼期待する出力
# ```
# l1はl2に含まれている : False
# l2はl1に含まれている : True
# ```

l1 = [1, 2, 3, 4, 5]
l2 = [3, 4, 5]
print(f'l1はl2に含まれている : {set(l1).issubset(set(l2))}')    # l1はl2に含まれている : False
print(f'l2はl1に含まれている : {set(l2).issubset(set(l1))}')    # l2はl1に含まれている : True