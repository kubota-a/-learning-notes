# 問題60
# 以下2つのリストから重複を削除して、すべての要素を出力するプログラムを作成してください。
# ※使用するリスト
# ```
# l1 = [1, 2, 3, 4, 5]
# l2 = [7, 6, 5, 4]
# ```
# ▼期待する出力
# ```
# すべての要素 : {1, 2, 3, 4, 5, 6, 7}
# ```

# 設計メモ
# l1とl2を足し合わせたリストをsetにキャストしてみる

l1 = [1, 2, 3, 4, 5]
l2 = [7, 6, 5, 4]
result = set(l1 + l2)
print(f'すべての要素 : {result}')    # すべての要素 : {1, 2, 3, 4, 5, 6, 7}

# 模範解答　集合の足し算　「｜」バーティカルバーを使用
l1 = [1, 2, 3, 4, 5]
l2 = [7, 6, 5, 4]
result = set(l1) | set(l2)
print(f'すべての要素 : {result}')    # すべての要素 : {1, 2, 3, 4, 5, 6, 7}

