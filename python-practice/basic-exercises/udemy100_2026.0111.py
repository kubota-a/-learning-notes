# 問題72
# リストに含まれている要素に対応するValueを、辞書から取り出し出力するプログラムを作成してください。
# なお、Keyが存在しないときはエラーではなく`None`を出力するようにしてください。
# ※使用する辞書とリスト
# ```
# d = {'A': 111, 'B': 222, 'C': 333}
# l = ['B', 'C', 'D', 'A']
# ```
# ▼期待する出力
# ```
# Bに対応するValue : 222
# Cに対応するValue : 333
# Dに対応するValue : None
# Aに対応するValue : 111
# ```

# 設計メモ
# Keyが存在しないときにエラーではなく`None`を出力する　→　getメソッド
# lをforループでまわして、対応するdのValueをgetメソッドで取得する

d = {'A': 111, 'B': 222, 'C': 333}
l = ['B', 'C', 'D', 'A']
for k in l:
    v = d.get(k)
    print(f'{k}に対応するValue : {v}')

