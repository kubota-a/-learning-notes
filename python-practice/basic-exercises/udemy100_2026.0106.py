# 問題28（2周目）◎
# リスト` [1, 5, 3, 2, 4]`に格納されている数値の最大値を出力するプログラムを作成してください。
# ※組み込み関数を使わずに解いてみてください。
# ``
# リスト内の最大値 : 5
# ```

# 設計メモ
# ※最初に仮定した最大値を、forループで比較して更新していくことにする
# [1, 5, 3, 2, 4]を要素とするlを宣言
# とりあえず最大値を先頭の要素にしておくための変数max_numに、lの先頭の要素を格納
# for文でlの要素をインデックス1から最後まで1つずつ取り出し、変数iに格納
# もしiがmax_numよりも大きい場合、max_numをiに更新する
# ループ終了後、「リスト内の最大値 : 」とmax_numをprintで出力

l = [1, 5, 3, 2, 4]
max_num = l[0]
for i in l[1:]:
    if i > max_num:
        max_num = i
print(f'リスト内の最大値 : {max_num}')

print("-" * 20)# ----------
