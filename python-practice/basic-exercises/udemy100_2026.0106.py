# 問題26（2周目）△
# リスト`[1, 2, 3, 4, 5]`に格納されている数値を足し合わせるプログラムを作成してください。
# ※組み込み関数を使わずに解いてみてください。
# ```
# リスト内の合計 : 15
# ```

# 設計メモ
# ※単にfor文で1つずつ自分自身に足し合わせていく！
# ```
# [1, 2, 3, 4, 5]を要素とするlを宣言
# 変数sumを0で初期化して宣言
# for文でlの要素を1つずつ取り出し、変数iに格納
# sumをsum+iで更新
# ループ終了後、「リスト内の合計 :」とsumをprintで出力

l = [1, 2, 3, 4, 5]
total = 0
for i in l:
    total += i
print(f'リスト内の合計 :{total}')

