# 問題30（2周目）△
# リスト`[1, 2, 2, 3, 3, 4, 5]`から重複している値を削除するプログラムを作成してください。
# ```
# 重複削除したリスト : [1, 2, 3, 4, 5]
# ```

# 設計メモ
# ※set使わないバージョン
# [1, 2, 2, 3, 3, 4, 5]を要素とするlを宣言
# 空のリストnew_lを宣言
# for文でlの要素を1つずつ取り出し、変数iに格納
# もしiがnew_lに含まれていない場合、iをnew_lの要素として追加
# ループ終了後、「重複削除したリスト : 」とnew_lをprintで出力

l = [1, 2, 2, 3, 3, 4, 5]
new_l = []
for i in l:
    if i not in new_l:
        new_l.append(i)
print(f'重複削除したリスト : {new_l}')

