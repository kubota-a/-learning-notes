# 問題16
# 入力した文字列と、その長さを出力するプログラムを作成してください。
# ※組み込み関数を使わずに解いてみてください。
# 文字を入力してください > Python
# Pythonの文字数 : 6

# 設計メモ
# ※組み込み関数len()を使用せず文字列の長さを数える　→　for文で1つずつ取り出してカウンタ変数で数えることにする 
# input関数で「文字を入力してください > 」という文字列と入力BOXを設置し、入力された文字列を変数sに格納
# カウンタ変数countを0で初期化して宣言
# for文でsを1つずつ取り出して変数iに格納
# countに、「count+1」をしたものを格納
# for文から出たら、「Pythonの文字数 : 」とcountをprintで出力

s = input("文字を入力してください > ")
count = 0
for i in s:
    count += 1
print(f'{s}の文字数 : {count}')

# もっとスマートなコード
s = input("文字を入力してください > ")
count = 0
for _ in s:    # 上記の「i」のような使い捨ての変数（後で使わない変数）は、アンダースコアで代用したほうが良い
    count += 1
print(f'{s}の文字数 : {count}')