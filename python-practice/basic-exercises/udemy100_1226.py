# 1つ目の整数を入力してください > 2
# 2つ目の整数を入力してください > 4
# 計算結果 : 2

# 設計メモ
# ※input関数から受け取った文字列aとbをどうやって計算するのか、どうしてもわからないので組み込み関数だけどint()使って書いてみる
# input関数で「1つ目の整数を入力してください > 」という文字列と入力BOXを設置し、入力された文字列を変数aに格納、それを整数化する
# input関数で「2つ目の整数を入力してください > 」という文字列と入力BOXを設置し、入力された文字列を変数bに格納、それを整数化する
