# 1以上の整数aを入力したら、下記のようにa + aaaを計算するプログラムを作成してください。
# ※例 : a = 2の場合：2 + 222 = 224

# 整数を入力してください > 2
# 計算結果 : 224

# 設計メモ 
# ※inputの入力BOXから受け取った値をいきなりint型にキャストするんじゃなくて、いったん文字列のままにしておいたほうが「aaa」を作成しやすい
# inputで「整数を入力してください > 」という表示と入力BOXを用意し、入力BOXに入った文字列をそのまま変数n_strに格納
# 「int型にキャストしたn_str」に、「int型にキャストした n_str × 3」を足し合わせ、変数resultに格納する
# ※上記の工程、当初は「n_str」に「n_str × 3」を足し合わせた後でまとめてint型にキャストしようと思っていて、途中で間違いに気が付いて修正した
# 「計算結果 : 」とresultを、フォーマット文字列でprint出力

n_str = input("整数を入力してください > ")
