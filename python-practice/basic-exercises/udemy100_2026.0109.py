# 問題48
# 以下のリストで電話番号が080で始まる要素だけ取り出すプログラムを作成してください。(※適当に作成した電話番号なので、電話しないようにしてください。)
# ※使用するリスト : 
# ```
# telephone_numbers = ['080-1203-4455', '090-9372-9682', '090-3080-4982', '080-3917-5918']
# ```
# ▼期待する出力
# ```
# 080で始まる電話番号 : ['080-1203-4455', '080-3917-5918']
# ```

# 設計メモ
# ※「～で始まる要素」を特定するには、文字列のstr.startswith(先頭の文字列)メソッドを使う　※兄弟メソッドにendswith()メソッドが存在する
# ※str.startswith(先頭の文字列)で、trueかfalseが返ってくる
# フィルタリングして取り出すだけだから、リスト内包表記で書いてみることにする
# ```
# telephone_numbers = ['080-1203-4455', '090-9372-9682', '090-3080-4982', '080-3917-5918']を宣言
# telephone_numbersの要素の中で、「080で始まる要素だけ」を格納した新しいリストnew_telephone_numbersを定義
# 「 080で始まる電話番号 : 」とnew_telephone_numbersをprintで出力

telephone_numbers = ['080-1203-4455', '090-9372-9682', '090-3080-4982', '080-3917-5918']
new_telephone_numbers = [n for n in telephone_numbers if n.startswith("080")]
print(f'080で始まる電話番号 : {new_telephone_numbers}')

# 模範解答　「～で始まる要素」を特定するのにスライスを使う方法
telephone_numbers = ['080-1203-4455', '090-9372-9682', '090-3080-4982', '080-3917-5918']
new_telephone_numbers = [n for n in telephone_numbers if "080" in n[:3]]
print(f'080で始まる電話番号 : {new_telephone_numbers}')



