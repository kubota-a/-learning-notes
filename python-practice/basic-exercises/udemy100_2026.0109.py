# 問題47
# 以下のリストで、インデックス番号と格納されている値が、ともに3の倍数の要素を削除するプログラムを作成してください。
# ※使用するリスト : 
# ```
# l = [1, 3, 2, 3, 4, 6, 5, 8, 7]
# ```
# ▼期待する出力
# ```
# 削除後のリスト : [1, 3, 2, 4, 6, 5, 8, 7]
# ```

# 設計メモ
# リストの要素はきれいに整列しておらず、重複もある
# 　→　rangeでスキップ設定して処理、はできない　→　1つずつ、要素そのものとそのインデックス番号をif文で「3で割ったときの余りが0」かどうかで3の倍数判定を行う
# 　→　マジックナンバーは使わないでおく。なので最終的にいくつ要素が減るかとかもわからない条件だから、そもそもrange()では無理
# 要素数の減少による不具合を避けるため、for文で回すのはlそのもの以外にする
# 要素とインデックス番号を同時に取得するにはenumerate関数を使用
# ```

# 設計メモ　失敗  
# l = [1, 3, 2, 3, 4, 6, 5, 8, 7]を宣言
# lの要素の中から、「要素を3で割ったときの余りが0ではなく、かつ、要素のインデックス番号を3で割ったときの余りも0ではない」要素を選んで新しいリストnew_lに格納
# 「削除後のリスト : 」とnew_lをprintで出力
l = [1, 3, 2, 3, 4, 6, 5, 8, 7]
new_l = [v for i, v in enumerate(l) if i % 3 != 0 and v % 3 != 0]
print(f'削除後のリスト : {new_l}')    # 削除後のリスト : [2, 4, 8, 7]
# ※「インデックスが3の倍数ではなく、かつ、値も3の倍数ではない場合だけ新リストに追加する」設計になってるから、
# 　「インデックスだけが3の倍数」・「値だけが3の倍数」のものまで捨てられてしまってる

# 失敗　for文バージョン
# 設計メモ
# l = [1, 3, 2, 3, 4, 6, 5, 8, 7]を宣言
# lの要素の中から、「要素を3で割ったときの余りが0ではなく、かつ、要素のインデックス番号を3で割ったときの余りも0ではない」要素を選んで新しいリストnew_lに格納
# 「削除後のリスト : 」とnew_lをprintで出力
l = [1, 3, 2, 3, 4, 6, 5, 8, 7]
new_l = []
for i, v in enumerate(l):
    if i % 3 != 0 and v % 3 != 0:
        new_l.append(v)
print(f'削除後のリスト : {new_l}')    # 削除後のリスト : [2, 4, 8, 7]
# ※「インデックスが3の倍数ではなく、かつ、値も3の倍数ではない場合だけ新リストに追加する」設計になってるから、
# 　「インデックスだけが3の倍数」・「値だけが3の倍数」のものまで捨てられてしまってる

# ヒントもらって再チャレンジ
l = [1, 3, 2, 3, 4, 6, 5, 8, 7]
new_l = []
for i, v in enumerate(l):
    if i % 3 == 0 and v % 3 == 0:
        continue
    new_l.append(v)    # これなら、if条件以外の要素は全て新リストに格納できる
print(f'削除後のリスト : {new_l}')    # 削除後のリスト : [1, 3, 2, 4, 6, 5, 8, 7]

# 模範解答　再チャレンジのリスト内包表記バージョン
l = [1, 3, 2, 3, 4, 6, 5, 8, 7]
new_l = [v for i, v in enumerate(l) if not(i % 3 == 0 and v % 3 == 0)]
print(f'削除後のリスト : {new_l}')    # 削除後のリスト : [1, 3, 2, 4, 6, 5, 8, 7]

