# 問題40
# 以下のリストで、整数(int型)の最大値・最小値を出力するプログラムを「4行以下」で作成してください。
# ※使用するリスト : 
# ```
# l = [1, '22', 3, '444', 0.0, '5']
# ```
# ▼期待する出力
# ```
# 最大値 : 3
# 最小値 : 1
# ```

# 設計メモ
# ※整数型の判定はisinstance、最大値はmax、最小値はminの使用を検討
# isinstanceでlから整数型の要素のみ集めた新リストを作成し、max(新リスト)とmin(新リスト)を出力することにする
# ```
# l = [1, '22', 3, '444', 0.0, '5']を宣言
# isinstanceで整数型の要素のみ集めた新リストを作成し、変数l_intに格納
# 「最大値 : 」とmax(l_int)をprintで出力
# 「最小値 : 」とmin(l_int)をprintで出力

l = [1, '22', 3, '444', 0.0, '5']
l_int = [i for i in l if isinstance(i, int)]    # []付け忘れて構文エラーが出た
print(f'最大値 : {max(l_int)}')
print(f'最小値 : {min(l_int)}')

