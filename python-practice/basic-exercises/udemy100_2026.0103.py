# 問題41
# 以下のリストで「数字部分とインデックス番号が一致する要素だけ」をリスト形式で出力するプログラムを「3行以下」で作成してください。
# ※使用するリスト : 
# ```
# l = [0, '1', 3, 2, '4', 5, '7']
# ```
# ▼期待する出力
# ```
# インデックスと値が一致 : [0, '1', '4', 5]
# ```

# 設計メモ
# ※いったんfor文で考えると、イニュメレイトenumerate(l)をforループで回して、インデックス番号を変数iに、要素を変数vに格納
# ※vが整数型であればvそのものがiと一致するか、ｖが整数型でない場合はvを整数型にキャストしたものがiと一致するか判定
# ※条件合致した要素を新しいリストnew_lに追加
# ```
l = [0, '1', 3, 2, '4', 5, '7']
new_l = [v for i, v in enumerate(l) if int(v) == i]    # vがもともと整数型でも文字列型でも、一律int()でキャスト
print(f'インデックスと値が一致 : {new_l}')


